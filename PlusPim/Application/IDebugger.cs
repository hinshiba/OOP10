namespace PlusPim.Application;

internal interface IDebugger {
    void SetLogger(Action<string> log);
    bool Load(string programPath);
    int GetPC();
    int[] GetRegisters();  // 32要素の配列
    int GetHI();
    int GetLO();
    void Step();

    /// <summary>
    /// 1ステップ分、実行を巻き戻す
    /// </summary>
    /// <returns>巻き戻しに成功した場合は<see langword="true"/></returns>
    bool StepBack();

    /// <summary>
    /// 0から始まる現在実行前の行番号を取得する
    /// </summary>
    /// <returns>行番号 ただし何かしらの問題で無効であった場合は-1</returns>
    int GetCurrentLine();
    string GetProgramPath();
    bool IsTerminated();
}
